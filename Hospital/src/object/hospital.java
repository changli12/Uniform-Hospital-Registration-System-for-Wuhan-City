package object;

public class hospital {
	private int hos_id;
	private String hos_addr;
	private String tel;
	private String hos_name;
	private String region;
	private String hos_class;
	
	public int getHos_id() {
		return hos_id;
	}
	public void setHos_id(int hos_id) {
		this.hos_id = hos_id;
	}
	public String getHos_addr() {
		return hos_addr;
	}
	public void setHos_addr(String hos_addr) {
		this.hos_addr = hos_addr;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getHos_name() {
		return hos_name;
	}
	public void setHos_name(String hos_name) {
		this.hos_name = hos_name;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getHos_class() {
		return hos_class;
	}
	public void setHos_class(String hos_class) {
		this.hos_class = hos_class;
	}
}
